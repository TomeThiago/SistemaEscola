    public void readJTable(){
        DefaultTableModel dtmAlunos = (DefaultTableModel) jTable1.getModel();
        dtmAlunos.setNumRows(0);
        AlunoDAO alunodao = new AlunoDAO();
        
        for(Aluno a: alunodao.read()){
            Object[] aluno = {
                a.getCodigo(),
                a.getNome()
            };
            dtmAlunos.addRow(aluno);
        }
    }
    
